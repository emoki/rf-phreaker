			SETPRESENT_TRRShortPD_Data (data, ID_RRShortPD_VBS_VGCS_RECONFIGURE2);
			INIT_c_VBS_VGCS_RECONFIGURE2 (data->Data.fld_c_VBS_VGCS_RECONFIGURE2);
